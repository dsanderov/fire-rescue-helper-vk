menu_registry = {}


# =========================================
# РЕГИСТРАЦИЯ МЕНЮ
# =========================================

def register_menu(menu_name, handler):

    menu_registry[menu_name] = handler


# =========================================
# ПОЛУЧИТЬ HANDLER МЕНЮ
# =========================================

def get_menu_handler(menu_name):

    return menu_registry.get(menu_name)