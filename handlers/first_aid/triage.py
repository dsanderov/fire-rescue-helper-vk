from services.messages.sender import send_message


def handle_triage(vk, user_id):
    message = (
        "🚑 Сортировка пострадавших\n\n"

        "🚨 Признаки ситуации:\n"
        "• пострадавших несколько;\n"
        "• сил помощи недостаточно для одновременной помощи всем;\n"
        "• есть угроза жизни у части пострадавших;\n"
        "• требуется быстро определить приоритет помощи.\n\n"

        "✅ Что делать:\n"
        "1. Обеспечить безопасность места.\n"
        "2. Быстро оценить количество пострадавших.\n"
        "3. Вызвать дополнительные силы и скорую помощь.\n"
        "4. Выделить пострадавших, которые могут идти самостоятельно.\n"
        "5. В первую очередь оценивать сознание, дыхание и массивное кровотечение.\n"
        "6. Оказывать помощь тем, у кого есть непосредственная угроза жизни.\n"
        "7. Постоянно переоценивать состояние пострадавших.\n\n"

        "❌ Что делать нельзя:\n"
        "• тратить много времени на одного пострадавшего при большом числе пострадавших;\n"
        "• игнорировать безопасность места;\n"
        "• оставлять без внимания людей без сознания;\n"
        "• не сообщать диспетчеру о количестве пострадавших;\n"
        "• путать сортировку с окончательным диагнозом.\n\n"

        "📌 Важно:\n"
        "Сортировка нужна не для постановки диагноза, а для определения очередности помощи. "
        "Приоритет — безопасность, дыхание, кровотечение, сознание."
    )

    send_message(vk=vk, user_id=user_id, message=message)